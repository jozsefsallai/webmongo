<h1>About WebMongo</h1>

<section class="content">
  <p>
    <strong>WebMongo</strong> is a fast and secure web-based GUI for managing
    MongoDB servers with ease from nearly any environment. It works by saving
    your connection details to your browser's local storage, so nothing is
    ever being saved on a remote database.
  </p>
  <p>
    To sync things up, you can use the <strong>Import/Export</strong> feature.
    The exported file is encrypted using a passphrase of your choice, so you
    can carry it around and import it on other devices without worrying about
    someone gaining access to your connection strings.
  </p>
  <p>
    The app also provides an easy way to lock your connection strings using a
    passphrase, which will basically encrypt the info that's saved in your
    local storage and keep it like that until you unlock it with the same
    passphrase. For security reasons, you can also delete all your connection
    strings with two clicks.
  </p>
  <p>
    WebMongo is an <a href="https://github.com/jozsefsallai/webmongo"
    target="_blank" rel="noopener">open-source</a> app.
  </p>
</section>
